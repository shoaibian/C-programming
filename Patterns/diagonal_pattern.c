#include<stdio.h>
int main()
{
    int i,j,k;
    for(i=1;i<=5;i++)
    {
        for(j=1;j<=5;j++)
        {
            (i<=3)?(k=i):(k=6-i);
            if(j<=3-k||j>=3+k)
            {
              printf("  ");  
            }
            else
            {
                printf(" *");
            
            }
        }
           printf("\n");
    }
}
